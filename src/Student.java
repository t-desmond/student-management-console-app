public class Student {
  String name;
  int age;
  int id;
  char grade;
  
  public String getName() {
    return name;
  }
  public void setName(String name) {
    this.name = name;
  }
  public int getAge() {
    return age;
  }
  public void setAge(int age) {
    this.age = age;
  }
  public int getId() {
    return id;
  }
  public void setId(int id) {
    this.id = id;
  }
  public char getGrade() {
    return grade;
  }
  public void setGrade(char grade) {
    this.grade = grade;
  }
}
